public enum AudioCategory
{
    Footstep,     // 脚步：走路、跑步
    Movement,     // 移动类：跳跃、翻滚、闪避、爬楼梯、攀爬、落地、摔倒
    Weapon,       // 轻攻击：普通挥砍1~4
    HeavyAttack,  // 重攻击：蓄力重击1~4、冲刺
    Skill,        // 技能释放：神砍、石像变身、钉墙、血刃发射、召唤、无敌、时停
    Projectile,   // 投射物飞行：血刃飞行、石头飞行、炮弹飞行、光球飞行、子弹飞行
    Impact,       // 命中/爆炸：投射物命中、炮弹爆炸、武器砍中肉体的反馈
    Hit,          // 受击：玩家受伤、敌人受伤
    Death,        // 死亡 & 复活：玩家死亡、敌人死亡、Boss死亡、复活
    Item,         // 道具：喝药、拾取道具、拾取金币
    Mechanism,    // 环境交互：机关触发、机关移动、传送门
    UI,           // 界面音效：按钮点击、商店开关、购买、对话推进、血条警告
    BGM,          // 背景音乐
    Ambient,      // 环境氛围：风声、水声、场景底噪
    Voice         // 语音：角色喊叫、Boss台词（预留）
}
